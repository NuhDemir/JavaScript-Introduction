//Bit işlem Operatörleri
// Bitsel VE
var a = 5; //0101
var b = 3; //0011

var result = a & b; //0001

// Bitsel VEYA
var result = a | b; // 0111

//Bitsel ÖzEL VEYA
var result = a ^ b; // 0110

//Bitsel DEĞİL
var result = ~ a ; // 1010

//Sola Kaydırma
var result = a << 1; // 1010

//Sğa kaydırma
var result = a >> 1; // 0010

//Sağa kaydırma(işareti koru)
var result = a >>> 2; //0010


