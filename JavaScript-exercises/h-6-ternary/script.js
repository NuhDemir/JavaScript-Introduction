const day = "Pazartesi";

switch (day) {
  case "Pazartesi":
    console.log("Haftanın ilk günü!");
    break;
  case "Salı":
    console.log("Haftanın ikinci günü.");
    break;
  case "Çarşamba":
    console.log("Haftanın üçüncü günü.");
    break;
  default:
    console.log("Geçersiz gün ismi!");
}